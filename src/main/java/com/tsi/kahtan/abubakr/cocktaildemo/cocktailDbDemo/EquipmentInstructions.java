package com.tsi.kahtan.abubakr.cocktaildemo.cocktailDbDemo;

import com.google.gson.Gson;

import java.util.ArrayList;
import java.util.List;

public class EquipmentInstructions {
    private List<EquipmentInstructions> equipmentInstructionsList = new ArrayList<>();
    private int instructionID;
    private int equipmentID;

    public EquipmentInstructions(int instructionID, int equipmentID){
        this.instructionID = instructionID;
        this.equipmentID = equipmentID;
    }

    public void populatingIngredientList(){
        this.equipmentInstructionsList.add(new EquipmentInstructions(0, 0));
    }
    public List<EquipmentInstructions> getEquipmentInstructionsList(){
        return this.equipmentInstructionsList;
    }
    public String toString(){
        String json = new Gson().toJson(equipmentInstructionsList);
        return json;
    }
}
