package com.tsi.kahtan.abubakr.cocktaildemo.cocktailDbDemo;

import com.google.gson.Gson;

import java.util.ArrayList;
import java.util.List;

public class CocktailInstructions {

    private List<CocktailInstructions> cocktailInstructionsList = new ArrayList<>();
    private int cocktailID;
    private int instructionID;
    private int stepNumber;

    public CocktailInstructions(int cocktailID, int stepNumber, int instructionID){
        this.cocktailID = cocktailID;
        this.stepNumber = stepNumber;
        this.instructionID = instructionID;
    }

    public void populatingIngredientList(){
        this.cocktailInstructionsList.add(new CocktailInstructions(0, 0, 0));
    }
    public List<CocktailInstructions> getCocktailInstructionsList(){
        return this.cocktailInstructionsList;
    }
    public String toString(){
        String json = new Gson().toJson(cocktailInstructionsList);
        return json;
    }
}